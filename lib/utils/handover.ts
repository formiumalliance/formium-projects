// Generates downloadable handover ZIP package

import { prisma } from '@/lib/db/prisma'
import archiver from 'archiver'
import { PassThrough } from 'stream'

export async function generateHandoverPackage(projectId: string): Promise<Buffer> {
  const project = await prisma.project.findUnique({
    where: { id: projectId },
    include: {
      handover: true,
      documents: {
        where: {
          type: { in: ['AGREEMENT', 'INVOICE', 'HANDOVER'] },
        },
        include: { attachments: true },
      },
      tasks: {
        where: { status: 'DONE' },
        select: { title: true, phase: true, completedAt: true },
      },
      milestones: {
        where: { isCompleted: true },
        select: { title: true, completedAt: true },
      },
      clientProfile: {
        include: { user: { select: { name: true, email: true } } },
      },
      projectManager: { select: { name: true, email: true } },
    },
  })

  if (!project) throw new Error('Project not found')

  return new Promise((resolve, reject) => {
    const chunks: Buffer[] = []
    const passThrough = new PassThrough()
    const archive = archiver('zip', { zlib: { level: 9 } })

    passThrough.on('data', (chunk) => chunks.push(chunk))
    passThrough.on('end', () => resolve(Buffer.concat(chunks)))
    passThrough.on('error', reject)
    archive.on('error', reject)
    archive.pipe(passThrough)

    // README
    const readme = generateReadme(project)
    archive.append(readme, { name: 'README.md' })

    // Project Summary
    const summary = generateProjectSummary(project)
    archive.append(summary, { name: 'project-summary.md' })

    // Credentials (if any)
    if (project.handover?.credentialsNotes) {
      const credentials = `# Credentials & Access Details\n\n${project.handover.credentialsNotes}\n\n⚠️  Keep this file secure. Delete after storing credentials in a password manager.\n`
      archive.append(credentials, { name: 'credentials/CREDENTIALS.md' })
    }

    // Deployment Info
    if (project.handover) {
      const deployment = generateDeploymentDoc(project.handover)
      archive.append(deployment, { name: 'deployment/DEPLOYMENT.md' })
    }

    // Task completion log
    const taskLog = generateTaskLog(project.tasks)
    archive.append(taskLog, { name: 'delivery/TASK_LOG.md' })

    // Milestone log
    const milestoneLog = generateMilestoneLog(project.milestones)
    archive.append(milestoneLog, { name: 'delivery/MILESTONES.md' })

    archive.finalize()
  })
}

