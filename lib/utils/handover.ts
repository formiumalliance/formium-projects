function generateTaskLog(tasks: any[]): string {
  const byPhase = tasks.reduce((acc: any, task: any) => {
    if (!acc[task.phase]) acc[task.phase] = []
    acc[task.phase].push(task)
    return acc
  }, {})

  let log = `# Task Completion Log\n\nTotal tasks completed: ${tasks.length}\n\n`
  
  for (const [phase, phaseTasks] of Object.entries(byPhase)) {
    log += `## ${phase.replace(/_/g, ' ')}\n\n`
    for (const task of (phaseTasks as Array<{ title: string; phase: string; completedAt?: Date | null }>)) {
      log += `- [x] ${task.title}${task.completedAt ? ` _(${new Date(task.completedAt).toLocaleDateString('en-IN')})_` : ''}\n`
    }
    log += '\n'
  }

  return log
}

function generateMilestoneLog(milestones: any[]): string {
  let log = `# Milestones Achieved\n\n`
  for (const ms of milestones) {
    log += `- **${ms.title}** — ${ms.completedAt ? new Date(ms.completedAt).toLocaleDateString('en-IN') : 'Completed'}\n`
  }
  return log
}
